package com.jetbrains;

public class Student {
    private String name;
    private double rating;

    public String getName()
    {
        return this.name;
    }

    public double getRating()
    {
        return this.rating;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public void setRating(double rating)
    {
        this.rating = rating;
    }
}
